primer_nuemro = int(input("Dime tu primer numero: "))
segundo_numero = int(input("Dime tu segundo numero: "))

if primer_nuemro > segundo_numero:
    print(f"{primer_nuemro} es mayor que {segundo_numero}")

elif segundo_numero > primer_nuemro:
    print(f"{primer_nuemro} es menor que {segundo_numero}")